    private void carregaTabela(){
        String[] cabecalho = {"Codigo","Nome","Cpf","Rg","Endereço","Contato"};
        
        DefaultTableModel dtm = new DefaultTableModel(cabecalho,0);
        try{
            Iterator<ClientModel> it = new ClientController().list().iterator();
            while(it.hasNext()){
                
                ClientModel cm = it.next();
                String[] linha = {String.valueOf(cm.getCli_code()), cm.getCli_name(), cm.getCli_cpf(), cm.getCli_rg(), cm.getCli_address(), cm.getCli_contact()};
                dtm.addRow(linha);
            }
            tblClient.setModel(dtm);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao carregar tabela de ingredientes.\n" + e.getMessage());
        }
    }
    
