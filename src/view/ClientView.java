    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        ClientModel cm = new ClientModel();
        
        cm.setCli_address(txtAddress.getText());
        cm.setCli_contact(txtContact.getText());
        cm.setCli_cpf(txtCpf.getText());
        cm.setCli_name(txtName.getText());
        cm.setCli_rg(txtRg.getText());
        
        if(txtCode.getText().isEmpty()){
            cc.insert(cm);
        }else{
            cm.setCli_code(Integer.parseInt(txtCode.getText()));
            cc.update(cm);
        }
        txtAddress.setEnabled(false);
        txtContact.setEnabled(false);
        txtCpf.setEnabled(false);
        txtName.setEnabled(false);
        txtRg.setEnabled(false);
        txtAddress.setText(null);
        txtContact.setText(null);
        txtCpf.setText(null);
        txtName.setText(null);
        txtRg.setText(null);
        
        btnSave.setEnabled(false);
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
        carregaTabela();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientMouseClicked
        int linha =  tblClient.getSelectedRow();
        String id = tblClient.getValueAt(linha, 0).toString();
        
        ClientModel cm = new ClientModel();
        cm = cc.searchById(Integer.parseInt(id));
        
        txtCode.setText(Integer.toString(cm.getCli_code()));
        txtName.setText(cm.getCli_name());
        txtAddress.setText(cm.getCli_address());
        txtContact.setText(cm.getCli_contact());
        txtCpf.setText(cm.getCli_cpf());
        txtRg.setText(cm.getCli_rg());
        
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tblClientMouseClicked

