    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        StatusModel sm = new StatusModel();
        sm.setSta_name(txtName.getText());
        sm.setSta_code(Integer.parseInt(txtCode.getText()));
        sc.delete(sm);
        txtCode.setText(null);
        txtName.setText(null);
        txtName.setEnabled(false);
        
        btnDelete.setEnabled(false);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(false);
        
        carregaTabela();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void carregaTabela(){
        String[] cabecalho = {"Codigo","Nome"};
        
        DefaultTableModel dtm = new DefaultTableModel(cabecalho,0);
        try{
            Iterator<StatusModel> it = new StatusController().list().iterator();
            while(it.hasNext()){
                
                StatusModel sm = it.next();
                String[] linha = {String.valueOf(sm.getSta_code()), sm.getSta_name()};
                dtm.addRow(linha);
            }
            tblStatus.setModel(dtm);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao carregar tabela de culturas\n" + e.getMessage());
        }
    }
    
    
