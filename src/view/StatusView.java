    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StatusModel sm = new StatusModel();
        sm.setSta_name(txtName.getText());
        if(txtCode.getText().isEmpty()){
            sc.insert(sm);
        }else{
            sm.setSta_code(Integer.parseInt(txtCode.getText()));
            sc.update(sm);
        }
        txtCode.setText(null);
        
        txtName.setText(null);
        txtName.setEnabled(false);
        
        btnDelete.setEnabled(false);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(false);
        carregaTabela();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblStatusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStatusMouseClicked
        int linha =  tblStatus.getSelectedRow();
        String id = tblStatus.getValueAt(linha, 0).toString();
        
        StatusModel sm = new StatusModel();
        sm = sc.searchById(Integer.parseInt(id));
        
        txtCode.setText(Integer.toString(sm.getSta_code()));
        txtName.setText(sm.getSta_name());
        
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tblStatusMouseClicked

