    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
        int linha =  tblProduct.getSelectedRow();
        String id = tblProduct.getValueAt(linha, 0).toString();
        
        ProductModel pm = new ProductModel();
        pm = pc.searchById(Integer.parseInt(id));
        
        txtCode.setText(Integer.toString(pm.getPro_code()));
        txtName.setText(pm.getPro_name());
        txtPrice.setText(Double.toString(pm.getPro_price()));
        cbType.setSelectedItem(pm.getTm().getTyp_name());
        
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tblProductMouseClicked

