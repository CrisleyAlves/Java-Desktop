    private void carregaTabela(){
        String[] cabecalho = {"Codigo","Nome","Preço","Tipo"};
        
        DefaultTableModel dtm = new DefaultTableModel(cabecalho,0);
        try{
            Iterator<ProductModel> it = new ProductController().list().iterator();
            while(it.hasNext()){
                
                ProductModel pm = it.next();
                String[] linha = {String.valueOf(pm.getPro_code()), pm.getPro_name(), String.valueOf(pm.getPro_price()), pm.getTm().getTyp_name()};
                dtm.addRow(linha);
            }
            tblProduct.setModel(dtm);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao carregar tabela de produtos.\n" + e.getMessage());
        }
    }
    
